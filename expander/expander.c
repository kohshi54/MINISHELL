#include "expander.h"

/*
The order of word expansion shall be as follows:
Tilde expansion, parameter expansion, command substitution, 
and arithmetic expansion shall be performed, beginning to end.
Field splitting (see Field Splitting) shall be performed 
on the portions of the fields generated by step 1, unless IFS is null.
Pathname expansion (see Pathname Expansion) shall be performed, 
unless set -f is in effect.
Quote removal (see Quote Removal) shall always be performed last.

In minishell, following procedure is required:
Parameter expansion.
Field splitting.
Quote removal.
*/

t_node	*expand(t_node *root)
{
	expand_params(root);
	split_field(root);
	remove_quote(root);
	return (root);
}
